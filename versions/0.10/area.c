//Restituisce un elemento della mappa
char getSquareMap(int x, int y) {
    return areaGioco[x][y];
}

//Controlla se il una data posizione non vi Ã¨ un muro
bool isValidSquare(int x, int y) {
    if(getSquareMap(x, y) == ' ' || getSquareMap(x, y) == '~' 
        || getSquareMap(x, y) == '*')
        return TRUE;
    return FALSE;
}

void setSquareMap(int x, int y, char c) {
    if(isValidSquare(x, y))
        areaGioco[x][y] = c;
} 